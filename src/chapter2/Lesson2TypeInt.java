package chapter2;

public class Lesson2TypeInt {
    public static void main(String[] args) {
        int number = 20;
        int number_square = number * number;
        System.out.println("Type number INT and value = " + number);
        System.out.println("Square of the " + number + " = " + number_square);
        int new_number = 25;
        System.out.println("Type number INT and  value = " + new_number);
        int sum_numbers = new_number + number;
        System.out.println("Sum of two number = " + sum_numbers);
    }
}
